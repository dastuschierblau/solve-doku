export function fixCase(str) {
  let newStr = str.split(''),
    len = newStr.length,
    regEx = /[A-Z]/,
    aux = [],
    sliceInd = 0;

  newStr[0] = newStr[0].toUpperCase();
  for (let i = 1; i <= len; i++) {
    if (regEx.test(newStr[i])) {
      aux.push(newStr.slice(sliceInd, i).join(''));
      sliceInd = i;
    }
  }
  aux.push(newStr.slice(sliceInd, len).join(''));

  newStr = aux.join(' ');

  return newStr;
}

